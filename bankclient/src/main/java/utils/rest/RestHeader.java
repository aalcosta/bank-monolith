package utils.rest;

/**
 * Agnostic representation of HttpHeader.
 * 
 * Created by alexandre on 1/27/17.
 */
public class RestHeader {

    public String name;
    public String value;

    public RestHeader() {
        this.name = name;
        this.value = value;
    }

    public RestHeader(String name, String value) {
        this.name = name;
        this.value = value;
    }
}
